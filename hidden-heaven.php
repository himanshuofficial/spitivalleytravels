<h4>Day 01:- Shimla To Sangla </h4>
<p>
In the morning drive to Sangla. Blessed with mesmerizing scenery, Sangla is also known as Sangla Valley, offers superb views of the snow-capped peaks bounding it. As River Baspa traverses through the valley, it is called Baspa Valley too. The place of coronation of the King of Kinnaur, Kamru Fort is the most popular attraction in Sangla. Upon reaching, check-in at a Hotel. Take rest. Later, visit village Kamru, the original capital of Bushahr Kingdom. Here, you can visit the Kamru Fort, erected in notable tower-style architecture. The fort also has a temple that is dedicated to Kamakhya Devi.Overnight in hotel.
</p>
<h4>
Day 02 :-Sangla –Chitkul- Kalpa
</h4>
<p>
After having a hot, fresh breakfast while basking in the first rays of the sun, we head towards Chitkul. Chitkul can be described as one of the most scenic settlements in the valley and the last inhabited village before the India-China border. This short drive from Sangla to Chitkul will be one of the most scenic drives you’ll take with plenty of opportunities to take stunning photographs. Walk around the Baspa River and pay tributes to the Mathi Goddess at the temple in the village. After spending time in Chitkul, we turn around and head to Kalpa. Legend has it that Kalpa was the winter home of Lord Shiva. If it’s true, he couldn’t have found a more remarkable abode for himself, with enthrallingly majestic views of the Kinner Kailash and Jorkandan Peaks looming right above the village. Overnight at a hotel in Kalpa.
</p>
<h4>
DAY 03:  Kalpa – Nako
</h4>
<p>
After Breakfast in Hotel we continue driving on the famous Hindustan – Tibet Highway or the NH 22 – This stretch famously known as one of “most treacherous roads of the world” by the lonely planet in a visual spectacle. Don’t let that little piece of information deter you because your local drivers are highly skilled and experienced while driving on these roads. As you begin embracing the jaw-dropping views watch the mountains change their colours from the lush greens of the Kinnaur valley to the many hues of brown.In Kalpa visit the Suicide Point, which is located around 3 Kms away from Kalpa village and the journey to this point takes place through beautiful apple gardens. Dinner and Overnight Stay in Nako.</p>

<h4>
 DAY 04: Nako- Sumdo- Tabo – Dhankar-Pin valley –Mudh (111 Kms 2 Hrs)
  </h4>
  <p>
Have an early breakfast and head to Mudh this day. Enroute visit the Tabo Monastery which is close to 1000 years old now. The murals, thanks and paintings are believed to have been made in a single night by the Gods themselves. You can also hike to the meditation caves, if time permits.  <br>
<b>Gue Mummy</b> – This mummy was found during an army excavation and was placed in a tomb in the year 1975. According to carbon dating, the mummy is belived to be around 500 to 600 years old.<br>
<b>Tabo Monastery</b> – This Monastery is close to 1000 years old now. The murals, thankas and paintings are believed to have been made in a single night by the Gods themselves. You can also hike to the meditation caves. <br>
<b>Dhankar Monastery</b>- Seat of the King in ancient times- the most beautiful setting of a monument which hangs on a cliff and overlooks the confluence of Spiti and Pin River. Hike to Dhankar Lake, if your lungs permit. After that transfer to Kaza. Visit Kaza bazaar and Monastery. <br>
<b>Mud Village</b>
Mud is the last village in Pin Valley that is accessible by motor able road. The road actually goes a little ahead of the village as well towards Bhabha Pass but I do not believe there is any notable village after Mud. This village more or less acts as the base for anyone traveling to Pin Valley, be it a road trip, casual trekking or scaling the Pin Parvati trek and Bhabha Pass trek. Dinner and overnight stay in Mudh Village.
</p>
<h4>
DAY 05: Mud – Dhankar – Kaza 
 </h4>
 <p>
After breakfast check out from Home-stay/Hotel and drive to Kaza. Check in hotel and rest. After that visit Kaza Market and monastery. Dinner and overnight stay at Kaza.
 </p>
 <h4>
 DAY 06:  Kaza - Hikkim – Komic – Langza – (70 kms/ 3 hours)
 
</h4>
<p>
Take the morning easy and today we head to Langza- the main area for Fossils, <br>
<b>Hikkim</b>- the highest post office in the world. Visit the postmaster of the highest post office in the world at Hikkim and mail some postcards from here.<br>
<b>Komic</b>- one of the highest villages in the world at 4600 meters. A visit to the Tangyud monastery in Komic is also worthwhile. You can see some snow laden peaks during this drive. <br>
<b>Langza</b>-To make this memorable, you can also hike from Langza to Komic- 3-4 hours. Overnight stay in Langza home stay.
</p>

<h4>
DAY 07: LANGZA – KEY MONASTERY – KIBBER – Chandertaal 

</h4>
<p>
After breakfast, visit Key monastery and Kibber village. <br>

<b>Key Gompa</b> is a Gelugpa sect monastery. Its hilltop position points to a post-thirteenth century construction. The three storied structure consists largely of storage rooms at the bottom level, while the middle floor houses the Dukhang- the assembly hall- and the uppermost floor has another temple and the head Lama’s quarters, on one side of an open terrace. The greatest wealth of Key Gompa lies in its collection of ‘Thangkas’ salvaged with difficulty from the devastating raids of the Dogars and the Sikhs in the 19thCentury. From here a scenic drive takes you into the pasture country of Kibber. The pastures are spread over hundreds of square kilometers. <br>

<b>Kibber</b> is small village and is the take off point for the popular trek to Tso Moriri in Ladakh. Further up six kilometers from Kibber, drive up to a still smaller village of Gete. Consisting of just six houses, it boasts of being the highest village in Asia connected by road.. Overnight stay in Chandrataal camps.
</p>

<h4>
DAY 08: Chandertaal to Manali

</h4>
<p>
After early breakfast we departure to Chandrataal. Chandrataal Lake is renowned for its stunning Himalayan beauty. The lake is accessible via a SUV (8 km drive) from a point after crossing Kunzum La pass. The accommodation here is basic and therefore one has to be adventure frame of mind before considering. After that drive to Manali. Dinner and overnight stay in Manali Hotel.
</p>

<h4>
DAY 09:  Manali Drop 


</h4>
<p>
After breakfast check out from hotel and drive to Manali Volvo Station
</p>
