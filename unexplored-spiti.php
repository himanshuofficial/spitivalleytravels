<h4>Day 01:- Shimla- Sangla </h4>

<p>In the morning drive to Sangla. Blessed with mesmerizing scenery, Sangla is also known as Sangla Valley, offers superb views of the snow-capped peaks bounding it. As River Baspa traverses through the valley, it is called Baspa Valley too. The place of coronation of the King of Kinnaur, Kamru Fort is the most popular attraction in Sangla. Upon reaching, check-in at a Hotel. Take rest. Later, visit village Kamru, the original capital of Bushahr Kingdom. Here, you can visit the Kamru Fort, erected in notable tower-style architecture. The fort also has a temple that is dedicated to Kamakhya Devi. Overnight in hotel.</p>

<h4>Day 02: Sangla –Chitkul- Nako</h4>
<p>
Have a nice breakfast with a view of the mountains and head to Chitkul .
After having a hot, fresh breakfast while basking in the first rays of the sun, we head
towards Chitkul. Chitkul can be described as one of the most scenic settlements in the valley and the last
inhabited village before the India-China border. This short drive from Sangla to Chitkul will
be one of the most scenic drives you’ll take with plenty of opportunities to take stunning
photographs. Walk around the Bapsa river and pay tributes to the Mathi Goddess at the
temple in the village. After spending time in Chitkul, we turn around and head to Nako.You shall meet the Sutlej river at Sainj and from there on it will be with you till Spiti. Overnight stay in Nako Hotel.
</p>
<h4>
Day 03: Leh – Nako- Sumdo- Tabo - Kaza (150 kms)
</h4>
<p>
Have an early breakfast and head to Kaza this day. The terrain will change after Pooh and you will see the end of the tree line. See the confluence of Spiti and Sutlej river at Khab. Enroute visit the Tabo Monastery which is close to 1000 years old now. The murals, thankas and paintings are believed to have been made in a single night by the Gods themselves. You can also hike to the meditation caves, if time permits. Visit Dhankar Monastery- Seat of the King in ancient times- the most beautiful setting of a monument which hangs on a cliff and overlooks the confluence of Spiti and Pin River. Hike to Dhankar lake, if your lungs permit. After that transfer to Kaza. Dinner and overnight stay at Kaza.<br>

<b>Gue Mummy</b> – This mummy was found during an army excavation and was placed in a tomb in the year 1975. According to carbon dating, the mummy is belived to be around 500 to 600 years old.
</p>

<h4>Day 04: Kaza - Hikkim – Komic – Langza – (70 kms/ 3 hours)</h4>
<p>
Take the morning easy and today we head to Langza- the main area for Fossils, Hikkim- the highest post office in the world and Komic- one of the highest villages in the world at 4600 meters. A visit to the Tangyud monastery in Komic is also worthwhile. You can see some snow laden peaks during this drive. To make this memorable, you can also hike from Langza to Komic- 3-4 hours. Visit the postmaster of the highest post office in the world at Hikkim and mail some postcards from here after that visit the oldest monastery in the region Key Gompa. Drive back to Langza. Overnight stay in Langza home stay.
</p>

<h4>DAY 05: LANGZA – KEY MONASTERY – KIBBER – Chandertaal </h4>
<p>
After breakfast, visit Key monastery and Kibber village. <br>
<b>Key gompa</b> is a Gelugpa sect monastery. Its hilltop position points to a post-thirteenth century construction. The three storied structure consists largely of storage rooms at the bottom level, while the middle floor houses the Dukhang- the assembly hall- and the uppermost floor has another temple and the head Lama’s quarters, on one side of an open terrace. The greatest wealth of Key Gompa lies in its collection of ‘Thangkas’ salvaged with difficulty from the devastating raids of the Dogars and the Sikhs in the 19th century. 
From here a scenic drive takes you into the pasture country of Kibber. The pastures are spread over hundreds of square kilometers. 

<b>Kibber</b> is small village and is the take off point for the popular trek to Tso Moriri in Ladakh. Further up six kilometers from Kibber, drive up to a still smaller village of Gete. Consisting of just six houses, it boasts of being the highest village in Asia connected by road. Drive back Kaza for lunch. Evening will be for relaxation. Night halt at Chandertaal.
</p>

<h4>DAY 06: Chandertaal to Manali</h4>
<p>
After early breakfast we departure to Chandertaal. Chandertaal Lake is renowned for its stunning Himalayan beauty. The lake is accessible via a SUV (8 km drive) from a point after crossing Kunzum La pass. The accommodation here is basic and therefore one has to be adventure frame of mind before considering. After that drive to Manali. Dinner and overnight stay in Manali Hotel.
</p>
<h4>
DAY 07: Manali Drop </h4>
<p>
After breakfast check out from hotel and drive to Manali Volvo Station.
</p>

